fun main() {
    // integer literal
    val number = 42
    println("The number is $number")

    // floating-point literal
    val pi = 3.14
    println("The value of pi is $pi")

    // boolean literal
    val isTrue = true
    println("The value of isTrue is $isTrue")

    // character literal
    val letter = 'A'
    println("The letter is $letter")

    // string literal
    val message = "Hello, world!"
    println(message)
}



// Alternatively 

fun main() {
    val a: Int = 10          // integer literal
    val b: Double = 3.14     // floating-point literal
    val c: Boolean = true    // boolean literal
    val d: Char = 'a'        // character literal
    val e: String = "Hello"  // string literal
    
    println(a)
    println(b)
    println(c)
    println(d)
    println(e)
}
